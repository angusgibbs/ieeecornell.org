<h2>About us</h2>
<p>Cornell IEEE is a student led branch of the national Institute of Electrical and Electronics Engineers (IEEE) within the Cornell's ECE department. Our mission is to build a strong, collaborative community of STEM students (undergraduate and graduate) and faculty at Cornell University by hosting social, networking, professional development, educational workshops and outreach events. We also regularly host corporate outreach events to provide companies access to Cornell ECE and CS students.</p>

<p>During the Spring 2015 semester, we were awarded the College of Engineering Alumni Association (CEAA) Academic Achievement Award due to our active presence on campus and involvement with the Cornell engineering community.  We continue to pursue our mission of fostering innovation, collaboration, and professional development with Cornell Engineering by extending our reach every semester.</p>  

<p>As a student branch we subscribe to the IEEE Core Values of trust, growth and nurturing, global community building, partnership, service to humanity and integrity in action.</p>

